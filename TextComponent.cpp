#include "TextComponent.h"

TextComponent::TextComponent(std::string text) : TextComponent(text, 0, 0)
{
}

TextComponent::TextComponent(std::string text, int x, int y) : TextComponent(text, x, y, Anchor::Left)
{
}

TextComponent::TextComponent(std::string text, Color color): TextComponent(text, color, 0, 0, Anchor::Left)
{
}

TextComponent::TextComponent(std::string text, int x, int y, Anchor anchor) : TextComponent(text, PaletteColors::Black, x, y, anchor)
{
}

TextComponent::TextComponent(std::string text, Color color, int x, int y, Anchor anchor)
{
    this->text = text;
    this->anchor = anchor;
    this->xpos = x;
    this->ypos = y;
    this->fontColor = color;
}

void TextComponent::draw()
{
    glColor(fontColor);
    if (anchor == Anchor::Left) {
        glRasterPos2i(xpos, ypos);
    }
    else if (anchor == Anchor::Right) {
        int stringWidth = text.length() * 10;
        glRasterPos2i(xpos - stringWidth, ypos);
    }
    else {
        int stringWidth = text.length() * 10;
        glRasterPos2i(xpos - stringWidth / 2, ypos);
    }
    printString();
}

void TextComponent::setText(std::string text)
{
	this->text = text;
}

void TextComponent::setAnchor(Anchor anchor)
{
    this->anchor = anchor;
}

void TextComponent::printString()
{
    for (int i = 0; i < text.length(); i++) {
        int character = text[i] - 65;
        glBitmap(8, 13, 0.0, 2.0, 10.0, 0.0, characters[character]);
    }
}

